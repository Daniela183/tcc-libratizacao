        <h3 class="tit">Sobre A Libratização </h3>
        <p class="tex">Libratização é um site desenvolvido para auxiliar professores da educação infantil no ensino da Língua  Portuguesa como uma segunda língua aos seus alunos surdos. O site visa fornecer  apoio de forma inclusiva na alfabetização através do uso da tecnologia, apresentando novas formas de inovação, projetados em seus testes que serão aplicados ao estudante. 
Sua estrutura é dividida em níveis sendo eles nível básico, nível intermediário e nível avançado. Os níveis são separados em temas iguais, dos quais são: Saudações, Família, Calendário e Cores, se diferenciando no grau de dificuldade projetados em seus testes. 
</br>O nível Básico apresenta conteúdo de alfabetização sobre temas básicos e iniciais de um possível processo de alfabetização.
</br>O Nível Intermediário é a fixação dos conteúdos do nível básico através da escrita de palavras por meio da visualização do sinal em Libras.
</br>O Nível Avançado tem como função a construção de frases que serão feitas com o auxílio da visualização do sinal em Libras como no nível intermediário
</p>
        
